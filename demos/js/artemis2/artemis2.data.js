// ── Interpolation ────────────────────────────────────────────────

/** Convert EME2000 array [X,Y,Z] to {x,y,z} object */
function h2c(d) {
  return { x: d[0], y: d[1], z: d[2] };
}

/** Catmull-Rom cubic interpolation between 4 points */
function catmullRom(p0, p1, p2, p3, t) {
  const t2 = t * t;
  const t3 = t2 * t;
  return {
    x: 0.5 * ((2*p1.x) + (-p0.x+p2.x)*t + (2*p0.x-5*p1.x+4*p2.x-p3.x)*t2 + (-p0.x+3*p1.x-3*p2.x+p3.x)*t3),
    y: 0.5 * ((2*p1.y) + (-p0.y+p2.y)*t + (2*p0.y-5*p1.y+4*p2.y-p3.y)*t2 + (-p0.y+3*p1.y-3*p2.y+p3.y)*t3),
    z: 0.5 * ((2*p1.z) + (-p0.z+p2.z)*t + (2*p0.z-5*p1.z+4*p2.z-p3.z)*t2 + (-p0.z+3*p1.z-3*p2.z+p3.z)*t3),
  };
}

/** Interpolate Orion position from NASA_TRAJ at given mission day */
function interpolateArray(data, day) {
  const N = data.length;
  if (day <= TRAJ_START_DAY) return h2c(data[0]);
  const maxDay = TRAJ_START_DAY + (N - 1) * TRAJ_STEP_DAYS;
  if (day >= maxDay) return h2c(data[N - 1]);

  const fIdx = (day - TRAJ_START_DAY) / TRAJ_STEP_DAYS;
  const i = Math.floor(fIdx);
  const t = fIdx - i;

  const i0 = Math.max(0, i - 1);
  const i1 = Math.min(N - 1, i);
  const i2 = Math.min(N - 1, i + 1);
  const i3 = Math.min(N - 1, i + 2);

  return catmullRom(h2c(data[i0]), h2c(data[i1]), h2c(data[i2]), h2c(data[i3]), t);
}

