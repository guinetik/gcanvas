/** Get Orion spacecraft position at mission day → {x,y,z} in km */
export function getOrionPos(day) {
  // Launch phase: lerp from Earth origin to first data point
  if (day < TRAJ_START_DAY) {
    const t = Math.max(0, day) / TRAJ_START_DAY; // 0..1
    const first = h2c(NASA_TRAJ[0]);
    return { x: first.x * t, y: first.y * t, z: first.z * t };
  }
  return interpolateArray(NASA_TRAJ, day);
}

/** Get Moon position at mission day → {x,y,z} in km */
export function getMoonPos(day) {
  return interpolateArray(NASA_MOON, day);
}

/** Get Orion velocity at mission day → km/s (scalar) */
export function getOrionVelocity(day) {
  const step = 0.002; // ~2.9 minutes
  const prev = getOrionPos(Math.max(0, day - step));
  const curr = getOrionPos(day);
  const dx = curr.x - prev.x;
  const dy = curr.y - prev.y;
  const dz = curr.z - prev.z;
  return Math.sqrt(dx*dx + dy*dy + dz*dz) / (step * 86400); // km per day → km/s
}

/** Get mission phase name at given day */
export function getPhase(day) {
  for (const p of PHASES) {
    if (day < p.end) return p.name;
  }
  return 'Mission Complete';
}

/** Get Moon orbit ring points (full sidereal month) → array of {x,y,z} */
export function getMoonOrbitPoints() {
  return MOON_ORBIT_FULL.map(h2c);
}

/**
 * Pre-generate smooth trajectory curve from ALL data points (including Earth orbit).
 * Uses Catmull-Rom with 10 subdivisions per segment.
 * @returns {Array<{x:number,y:number,z:number}>}
 */
export function buildTrajectoryCurve() {
  const pts = [];
  const SUBDIV = 10;

  // Launch phase: Earth origin → first data point (10 subdivisions)
  const first = h2c(NASA_TRAJ[0]);
  for (let s = 0; s < SUBDIV; s++) {
    const t = s / SUBDIV;
    pts.push({ x: first.x * t, y: first.y * t, z: first.z * t });
  }

  // JPL data segments
  for (let i = 0; i < TRAJ_N - 1; i++) {
    for (let s = 0; s < SUBDIV; s++) {
      const t = s / SUBDIV;
      const i0 = Math.max(0, i - 1);
      const i3 = Math.min(TRAJ_N - 1, i + 2);
      pts.push(catmullRom(h2c(NASA_TRAJ[i0]), h2c(NASA_TRAJ[i]), h2c(NASA_TRAJ[i + 1]), h2c(NASA_TRAJ[i3]), t));
    }
  }
  pts.push(h2c(NASA_TRAJ[TRAJ_N - 1]));
  return pts;
}

/**
 * Get index into the pre-built trajectory curve for given simDay.
 * Covers the full trajectory (Earth orbit + TLI + translunar + return).
 * @param {number} day - Current mission day
 * @param {number} totalPts - Length of the trajectory curve array
 * @returns {number} Integer index (0..totalPts-1)
 */
export function getTrajProgress(day, totalPts) {
  const LAUNCH_PTS = 10; // synthetic launch segment points

  if (day < TRAJ_START_DAY) {
    // Launch phase: 0..LAUNCH_PTS over 0..TRAJ_START_DAY
    const t = Math.max(0, day) / TRAJ_START_DAY;
    return Math.min(LAUNCH_PTS - 1, Math.max(0, Math.ceil(t * LAUNCH_PTS)));
  }

  // JPL data: offset by LAUNCH_PTS
  const dataIdx = (day - TRAJ_START_DAY) / TRAJ_STEP_DAYS;
  const trajIdx = LAUNCH_PTS + dataIdx * 10;
  return Math.min(totalPts - 1, Math.max(0, Math.ceil(trajIdx)));
}

/** 3D distance between two {x,y,z} points */
export function dist3d(a, b) {
  const dx = a.x - b.x;
  const dy = a.y - b.y;
  const dz = a.z - b.z;
  return Math.sqrt(dx*dx + dy*dy + dz*dz);
}

/**
 * Format simulation seconds as T+DD:HH:MM:SS.
 * @param {number} seconds
 * @returns {string}
 */
export function formatElapsed(seconds) {
  const s = Math.max(0, Math.floor(seconds));
  const ss = s % 60;
  const mm = Math.floor(s / 60) % 60;
  const hh = Math.floor(s / 3600) % 24;
  const dd = Math.floor(s / 86400);
  return `T+${String(dd).padStart(2,'0')}:${String(hh).padStart(2,'0')}:${String(mm).padStart(2,'0')}:${String(ss).padStart(2,'0')}`;
}

/** Format km with thousands separator */
export function formatKm(km) {
  return `${Math.round(Math.abs(km)).toLocaleString()} km`;
}
